package leetcode;

/**
 * problem:Implement int sqrt(int x)
 * @author chenbo
 */
public class Sqrt 
{
    static int sqrt(int x)
    {
        return (int)Math.sqrt(x);
    }
    public static void main(String[] args)
    {
        System.out.println(sqrt(668605518));
    }
}
 